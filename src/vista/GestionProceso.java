    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = txtNombre.getText();
        String descripcion = tarDescripcion.getText();
        String cantM = txtCantModulos.getText();
        int cantModulos = Integer.valueOf(cantM.toString());
        Especialidad unaEspecialidad = (Especialidad)cmbEspecialista.getSelectedItem();
        try {
            util.verificarNombre(nombre);
            util.verificarString(cantM, "Cantidad de Módulos");
            util.verificarCombos(cmbEspecialista.getSelectedItem().toString(), "Especialista");
            if(bandera.equals("nuevo")){
                this.cv.nuevoProceso(nombre, descripcion, cantModulos, isIcon, unaEspecialidad);
                TipoDiagnostico unTipoD = this.cv.traerTipoDiagnostico(codigoTipoD);
                this.cv.agregarProcesoADiagnostico(unTipoD, this.cv.recuperarUltimoProceso() ); 
                bandera = "";
                estadoInicio();
            }else{
                if(bandera.equals("editar")){
                    int codigo = Integer.valueOf(txtCodigo.getText());
                    this.cv.editarProceso(codigo, nombre, descripcion, cantModulos, true, unaEspecialidad);
                    estadoInicio();
                }
            }
            cargarTabla(tblProcesos, cv.traerProcesosConTipoDiagnostico(codigoTipoD));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void txtCantModulosKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantModulosKeyTyped
        char c = evt.getKeyChar();
        if (c < '0' || c > '9' || txtCantModulos.getText().length() > 1) {
            evt.consume();
        }
    }//GEN-LAST:event_txtCantModulosKeyTyped

    private void btnQuitarPiezaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarPiezaActionPerformed
        if (tblProcesos.getSelectedRow() != -1) {
            if (lstPRecambioProceso.getSelectedValue() != null) {
                try {
                    int codigo = Integer.valueOf(tblProcesos.getValueAt(tblProcesos.getSelectedRow(), 0).toString());
                    Proceso unProceso = cv.traerProceso(codigo);
                    PiezaRecambio miPieza = (PiezaRecambio)lstPRecambioProceso.getSelectedValue();
                    cv.quitarPiezaDeProceso(miPieza.getCodigo(), codigo);
                    List<PiezaRecambio> piezasSinV = cv.traerPiezasSinVinculoConProceso(unProceso);
                    List<PiezaRecambio> misPiezas = cv.traerPiezasConVinculoConProceso(unProceso);
                    DefaultListModel dpiezasConV = util.cargarLista(misPiezas);
                    DefaultListModel dpiezasSinV = util.cargarLista(piezasSinV);
                    lstPRecambioProceso.setModel(dpiezasConV);
                    lstPRecambio.setModel(dpiezasSinV);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(null, "Error: Seleccione un elemento de la lista de Piezas de Recambio del Proceso para quitar.");
            }
        }
    }//GEN-LAST:event_btnQuitarPiezaActionPerformed

    private void btnAgregarPiezaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarPiezaActionPerformed
        if (tblProcesos.getSelectedRow() != -1) {
            if (lstPRecambio.getSelectedValue() != null) {
                try {
                    int codigo = Integer.valueOf(tblProcesos.getValueAt(tblProcesos.getSelectedRow(), 0).toString());
                    Proceso unProceso = cv.traerProceso(codigo);
                    PiezaRecambio unaPieza = (PiezaRecambio) lstPRecambio.getSelectedValue();
                    cv.agregarPiezaAProceso(unaPieza, codigo);

                    List<PiezaRecambio> piezasSinV = cv.traerPiezasSinVinculoConProceso(unProceso);
                    List<PiezaRecambio> mispiezas = cv.traerPiezasConVinculoConProceso(unProceso);
                    DefaultListModel dPiezas = util.cargarLista(mispiezas);
                    DefaultListModel dPiezasSinV = util.cargarLista(piezasSinV);
                    lstPRecambioProceso.setModel(dPiezas);
                    lstPRecambio.setModel(dPiezasSinV);

                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(null, "Error: Seleccione un elemento de la lista de Piezas de Recambio para agregar.");
            }
        }
    }//GEN-LAST:event_btnAgregarPiezaActionPerformed

