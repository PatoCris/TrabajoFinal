    public void cargarTabla(JTable laTabla, List<Proceso> lista) throws Exception {
        miTabla = new DefaultTableModel();
        String cabecera[] = {"Código", "Nombre", "Descripción", "Cant. Modulos","Especialista"};
        miTabla.setColumnIdentifiers(cabecera);
        Object fila[] = new Object[miTabla.getColumnCount()];
        int cantidad = lista.size();
        if (cantidad > 0) {
            for (Proceso unProceso : lista) {
                fila[0] = unProceso.getCodigo();
                fila[1] = unProceso.getNombre();
                fila[2] = unProceso.getDescripcion();
                fila[3] = unProceso.getCantidadModulos();
                fila[4] = unProceso.getUnaEspecialidad();
                miTabla.addRow(fila);
            }
        }
        laTabla.setModel(miTabla);
    }

