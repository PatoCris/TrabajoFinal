    private void tblPiezaRecambioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPiezaRecambioMouseClicked
        try {
            if(tblPiezaRecambio.getSelectedRow() != -1){
                int codigo = Integer.valueOf(tblPiezaRecambio.getValueAt(tblPiezaRecambio.getSelectedRow(), 0).toString());
                txtCodigo.setText(tblPiezaRecambio.getValueAt(tblPiezaRecambio.getSelectedRow(), 0).toString());
                txtNombre.setText(tblPiezaRecambio.getValueAt(tblPiezaRecambio.getSelectedRow(), 1).toString());
                txtPrecio.setText(tblPiezaRecambio.getValueAt(tblPiezaRecambio.getSelectedRow(), 2).toString());
                txtImpuesto.setText(tblPiezaRecambio.getValueAt(tblPiezaRecambio.getSelectedRow(), 3).toString());
                TipoReparacion tipo = (TipoReparacion) tblPiezaRecambio.getValueAt(tblPiezaRecambio.getSelectedRow(), 4);//Traigo el tipo anomalia que está en la tabla
                cmbGrupoParte.getModel().setSelectedItem(tipo);//Seteo al modelo del combo el item seleccionado.
                if(tblPiezaRecambio.getValueAt(tblPiezaRecambio.getSelectedRow(), 5).toString().equals("SI")){
                    chkGarantia.setSelected(true);
                }else{
                    chkGarantia.setSelected(false);
                }
                PiezaRecambio unaPieza = cv.traerPiezaRecambio(codigo);
                txtMesesGarantia.setText(tblPiezaRecambio.getValueAt(tblPiezaRecambio.getSelectedRow(), 6).toString());
                tarCaracteristica.setText(unaPieza.getCaracteristica());
                lstCompatibles.setModel(utilVista.cargarLista(unaPieza.getVehiculosCompatibles()));
                lstModelos.setModel(utilVista.cargarLista(cv.traerModelosSinVinculo(unaPieza)));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_tblPiezaRecambioMouseClicked

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            int mesesGarantiaInt = 0;
            String nombre = txtNombre.getText();
            String precio = txtPrecio.getText();
            String impuesto = txtImpuesto.getText();
            boolean garantia = chkGarantia.isSelected();
            String mesesGarantia = txtMesesGarantia.getText();
            String caracteristica = tarCaracteristica.getText();
            
            utilVista.verificarNombre(nombre);
            utilVista.verificarString(precio, "Precio");
            utilVista.verificarString(impuesto, "Impuesto");
            if(chkGarantia.isSelected()){
                utilVista.verificarString(mesesGarantia, "Meses de Garantía");
                mesesGarantiaInt = Integer.valueOf(mesesGarantia);
            }
            utilVista.verificarCombos(cmbGrupoParte.getSelectedItem().toString(), "Grupo de Parte");
            
            double precioDouble = Double.valueOf(precio);
            double impuestoDouble = Double.valueOf(impuesto);
            TipoReparacion tipo = (TipoReparacion) cmbGrupoParte.getSelectedItem();

            if(bandera.equals("nuevo")){
                this.cv.nuevaPiezaRecambio(nombre, precioDouble, impuestoDouble, garantia, caracteristica, mesesGarantiaInt, true, tipo);
                bandera = "";
                estadoInicio();
                limpiar();
            }else{
                if(bandera.equals("editar")){
                    int codigo = Integer.valueOf(txtCodigo.getText());
                    this.cv.editarPiezaRecambio(codigo, nombre, precioDouble, impuestoDouble, garantia, caracteristica, mesesGarantiaInt, true, tipo);
                    estadoInicio();
                    limpiar();
                }
            }
            cargarTabla(tblPiezaRecambio, cv.traerPiezaRecambios(true)); //ACTUALIZAMOS LA TABLA
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

