    public void cargarTabla(JTable laTabla, List<PiezaRecambio> lista) throws Exception {
        miTabla = new DefaultTableModel();
        String cabecera[] = {"Código", "Nombre", "Precio", "Impuesto", "Grupo Parte", "Garantía", "Meses Garantía"};
        miTabla.setColumnIdentifiers(cabecera);
        Object fila[] = new Object[miTabla.getColumnCount()];
        int cantidad = lista.size();
        if (cantidad > 0) {
            for (PiezaRecambio piezaRecambio : lista) {
                fila[0] = piezaRecambio.getCodigo();
                fila[1] = piezaRecambio.getNombre();
                fila[2] = piezaRecambio.getPrecio();
                fila[3] = piezaRecambio.getImpuesto();
                fila[4] = piezaRecambio.getGrupoParte();
                if(piezaRecambio.isGarantia()){
                    fila[5] = "SI";
                }else{
                    fila[5] = "NO";
                }
                fila[6] = piezaRecambio.getMesesGarantia();
                miTabla.addRow(fila);
            }
        }
        laTabla.setModel(miTabla);
    }
