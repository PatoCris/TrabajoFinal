    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        try {
            if(tblClientes.getSelectedRow() != -1){
                int codigo = Integer.valueOf(tblClientes.getValueAt(tblClientes.getSelectedRow(), 0).toString());
                Cliente cliente = cv.traerCliente(codigo);
                cargarTablaVehiculo(tblVehiculos, cv.traerVehiculosSinCliente());
                cargarTablaVehiculo(tblVehiculosVinculados, cv.traerVehiculosConCliente(cliente));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_tblClientesMouseClicked

    private void btnVincularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVincularActionPerformed
        try {
            if(tblClientes.getSelectedRow() != -1 && tblVehiculos.getSelectedRow() != -1){
                
                int codigoCliente = Integer.valueOf(tblClientes.getValueAt(tblClientes.getSelectedRow(), 0).toString());
                int codigoVehiculo = Integer.valueOf(tblVehiculos.getValueAt(tblVehiculos.getSelectedRow(), 0).toString());
                Cliente cliente = cv.traerCliente(codigoCliente);
                Vehiculo vehiculo = cv.traerVehiculo(codigoVehiculo);
                cv.agregarVehiculoCliente(vehiculo,codigoCliente);
                cargarTablaVehiculo(tblVehiculos, cv.traerVehiculosSinCliente());
                cargarTablaVehiculo(tblVehiculosVinculados, cv.traerVehiculosConCliente(cliente));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } 
    }//GEN-LAST:event_btnVincularActionPerformed

    private void btnDesvincularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesvincularActionPerformed
        try {
            if(tblClientes.getSelectedRow() != -1 && tblVehiculosVinculados.getSelectedRow() != -1){
                
                int codigoCliente = Integer.valueOf(tblClientes.getValueAt(tblClientes.getSelectedRow(), 0).toString());
                int codigoVehiculo = Integer.valueOf(tblVehiculosVinculados.getValueAt(tblVehiculosVinculados.getSelectedRow(), 0).toString());
                Cliente cliente = cv.traerCliente(codigoCliente);
                Vehiculo vehiculo = cv.traerVehiculo(codigoVehiculo);
                cv.quitarVehiculoCliente(vehiculo, codigoCliente);
                //cv.borrarClienteVehiculo(codigoCliente, codigoVehiculo);
                cargarTablaVehiculo(tblVehiculos, cv.traerVehiculosSinCliente());
                cargarTablaVehiculo(tblVehiculosVinculados, cv.traerVehiculosConCliente(cliente));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } 
    }//GEN-LAST:event_btnDesvincularActionPerformed

