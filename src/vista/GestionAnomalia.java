    private void tblAnomaliasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAnomaliasMouseClicked
        try {
            if(tblAnomalias.getSelectedRow() != -1){
                TipoAnomalia tipo = (TipoAnomalia) tblAnomalias.getValueAt(tblAnomalias.getSelectedRow(), 2);//Traigo el tipo anomalia que está en la tabla
                cmbTipoAnomalia.getModel().setSelectedItem(tipo);//Seteo al modelo del combo el item seleccionado.
            
                String nivel = tblAnomalias.getValueAt(tblAnomalias.getSelectedRow(), 3).toString();
                cmbNivelS.getModel().setSelectedItem(nivel);
            }
            txtCodigo.setText(tblAnomalias.getValueAt(tblAnomalias.getSelectedRow(), 0).toString());
            tarDescripcion.setText(tblAnomalias.getValueAt(tblAnomalias.getSelectedRow(), 1).toString());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_tblAnomaliasMouseClicked


