    public void cargarTabla(JTable laTabla, List<Anomalia> lista) throws Exception {
        miTabla = new DefaultTableModel();
        String cabecera[] = {"Código", "Descripcion", "Tipo", "Nivel"};
        miTabla.setColumnIdentifiers(cabecera);
        Object fila[] = new Object[miTabla.getColumnCount()];
        int cantidad = lista.size();
        if (cantidad > 0) {
            for (Anomalia anomalia : lista) {
                fila[0] = anomalia.getCodigo();
                fila[1] = anomalia.getDescripcion();
                fila[2] = anomalia.getUnTipoAnomalia();
                fila[3] = anomalia.getNivelSeveridad();
                miTabla.addRow(fila);
            }
        }
        laTabla.setModel(miTabla);
    }
