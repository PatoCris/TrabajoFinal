    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if((!cmbNiveSBusqueda.getSelectedItem().toString().equals("[SELECCIONAR]")) || (!cmbTipoAnomaliaBusqueda.getSelectedItem().equals("[SELECCIONAR]"))){
            String nivel = "";
            TipoAnomalia tipo = null;
            if(!cmbNiveSBusqueda.getSelectedItem().toString().equals("[SELECCIONAR]")){
                nivel = cmbNiveSBusqueda.getSelectedItem().toString();
            }
            if(!cmbTipoAnomaliaBusqueda.getSelectedItem().toString().equals("[SELECCIONAR]")){
                tipo = (TipoAnomalia) cmbTipoAnomaliaBusqueda.getSelectedItem();
            }
            try {
                cargarTabla(tblAnomalias, cv.traerAnomaliasBusqueda(true, nivel, tipo));
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione un nivel de Severidad o un Tipo de Anomalia.");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

