    public void cargarTabla(JTable laTabla, List<Modelo> lista) throws Exception {
        miTabla = new DefaultTableModel();
        String cabecera[] = {"Código", "Nombre", "Descripcion", "Modelo", "Segmento"};
        miTabla.setColumnIdentifiers(cabecera);
        Object fila[] = new Object[miTabla.getColumnCount()];
        int cantidad = lista.size();
        if (cantidad > 0) {
            for (Modelo modelo : lista) {
                fila[0] = modelo.getCodigo();
                fila[1] = modelo.getNombre();
                fila[2] = modelo.getDescripcion();
                fila[3] = modelo.getUnaMarca();
                fila[4] = modelo.getUnSegmento();
                miTabla.addRow(fila);
            }
        }
        laTabla.setModel(miTabla);
    }

