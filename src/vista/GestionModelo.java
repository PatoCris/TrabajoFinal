    private void tblModelosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblModelosMouseClicked
        try {
            if(tblModelos.getSelectedRow() != -1){
                Marca marca = (Marca) tblModelos.getValueAt(tblModelos.getSelectedRow(), 3);//Traigo el tipo anomalia que está en la tabla
                cmbMarca.getModel().setSelectedItem(marca);//Seteo al modelo del combo el item seleccionado.

                Segmento segmento = (Segmento) tblModelos.getValueAt(tblModelos.getSelectedRow(), 4);//Traigo el tipo anomalia que está en la tabla
                cmbSegmento.getModel().setSelectedItem(segmento);//Seteo al modelo del combo el item seleccionado.
            }
            txtCodigo.setText(tblModelos.getValueAt(tblModelos.getSelectedRow(), 0).toString());
            txtNombre.setText(tblModelos.getValueAt(tblModelos.getSelectedRow(), 1).toString());
            tarDescripcion.setText(tblModelos.getValueAt(tblModelos.getSelectedRow(), 2).toString());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_tblModelosMouseClicked

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            String nombre = txtNombre.getText();
            String descripcion = tarDescripcion.getText();
            util.verificarNombre(nombre);
            util.verificarCombos(cmbMarca.getSelectedItem().toString(), "Marca");
            util.verificarCombos(cmbSegmento.getSelectedItem().toString(), "Segmento");
            Segmento segmento = (Segmento) cmbSegmento.getSelectedItem();
            Marca marca = (Marca) cmbMarca.getSelectedItem();

            if(bandera.equals("nuevo")){
                this.cv.nuevoModelo(nombre, descripcion, marca, segmento);
                bandera = "";
                estadoInicio();
                limpiar();
            }else{
                if(bandera.equals("editar")){
                    int codigo = Integer.valueOf(txtCodigo.getText());
                    this.cv.editarModelo(codigo, nombre, descripcion, marca, segmento);
                    estadoInicio();
                    limpiar();
                }
            }
            cargarTabla(tblModelos, cv.traerModelo(true)); //ACTUALIZAMOS LA TABLA
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

